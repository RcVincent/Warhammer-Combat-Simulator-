package model;

public class Weapon {
	private int Strength;
	private int AP;
	
	public Weapon() {
		
	}

	public int getStrength() {
		return Strength;
	}

	public void setStrength(int strength) {
		Strength = strength;
	}

	public int getAP() {
		return AP;
	}

	public void setAP(int aP) {
		AP = aP;
	}

}
