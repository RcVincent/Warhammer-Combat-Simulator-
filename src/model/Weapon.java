package model;

public class Weapon {
	private String Strength;
	private String AP;
	
	public Weapon() {
		
	}

	public String getStrength() {
		return Strength;
	}

	public void setStrength(String string) {
		Strength = string;
	}

	public String getAP() {
		return AP;
	}

	public void setAP(String string) {
		AP = string;
	}
	
	
}
